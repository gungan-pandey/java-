class student 
{
	public static void main(String[] arr)
	{
		int n,i;
		int s=0;
		Scanner sc=new Scanner(System.in);
		System.out.println("enter the size ");
		n=sc.nextInt();
		for(i=1;i<=n;i++)
		{
			s=s+(i/i+1);
		}
		System.out.println(s);
	}
}